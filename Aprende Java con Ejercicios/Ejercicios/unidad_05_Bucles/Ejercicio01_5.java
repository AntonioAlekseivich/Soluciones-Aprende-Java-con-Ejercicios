package unidad_05_Bucles;

public class Ejercicio01_5 {

	public static void main(String[] args) {
		// Muestra los números múltiplos 
		//de 5 de 0 a 100 utilizando un bucle for.

//si declaras la variable numero fuera del bucle no funciona
 
		for (int numero = 0 ; numero <= 100 ; numero += 5) {
			System.out.println(numero);
			}
	}

}
