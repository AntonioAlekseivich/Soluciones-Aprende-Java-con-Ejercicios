CREATE TABLE Permits (
    id_persona INT PRIMARY KEY,
    piramide NUMBER NOT NULL CHECK (piramide IN (0, 1)),
    tres_en_raya NUMBER NOT NULL CHECK (tres_en_raya IN (0, 1)),
    dado NUMBER NOT NULL CHECK (dado IN (0, 1)),
    FOREIGN KEY (id_persona) REFERENCES PERSONA(id_persona)
);

